export type DoodleIconName = keyof typeof doodleIconPaths

const iconModules = import.meta.glob('../assets/icons/**/*.svg')

export async function loadIcon(name: keyof typeof doodleIconPaths) {
  const relativePath = doodleIconPaths[name]
  const fullPath = `../assets/icons/${relativePath}.svg`
  // console.log('Loading icon:', fullPath,relativePath, name)

  const importer = iconModules[fullPath]
  if (!importer) {
    throw new Error(`Icon not found in iconModules: ${fullPath}`)
  }

  const mod = await importer()
  return mod
}