<script setup lang="ts">
import { computed, ref, watch } from 'vue'
import { type LoginUser } from '@zod/user/user.types'
import { otpRegex } from '@/lib/utils'

import DoodleIcons from '@/components/icons/DoodleIcons.vue'

const props = defineProps<{
  user: LoginUser
  isLoading: boolean
}>()

const emit = defineEmits<{
  (e: 'otp:submit', otp: string): void
}>()

// Reactive variables
const otpInput = ref('')
const error = ref('' as string)

// Method to handle OTP entered
async function handleOTPEntered() {
  emit('otp:submit', otpInput.value)
}

const inputState = computed(() => {
  // console.log('Validating OTP:', otpInput.value)
  if (!otpInput.value) return null
  // Simple validation for OTP: must be a number and 6 digits long
  return otpRegex.test(otpInput.value)
})

const validated = computed(() => {
  // console.log('Validating Auth ID state:', state.value)
  return !!(otpInput.value !== '' && inputState.value)
})

watch(inputState, state => {
  if (state === true) {
    emit('otp:submit', otpInput.value)
  }
})
</script>

<template>
  <div class="otp-form">
    <div class="fs-4 mb-3">
      <span class="text-muted opacity-50">
        <span v-if="user.phonenumber">
          <DoodleIcons name="IconMessage" class="svg-icon" />
        </span>
        <span v-else>
          <DoodleIcons name="IconMail" class="svg-icon" />
        </span>
      </span>
      Check your messages
    </div>
    <div class="mb-3 form-text mb-3">
      <div v-if="user.phonenumber">
        We have sent you a little code to your phone number, please enter it below.
      </div>
      <div v-else>
        We have sent you a login link, check your inbox. If you don't see it, please check your spam
        folder.
      </div>
    </div>

    <BForm
      @submit.prevent="handleOTPEntered"
      class="otp-form"
      :novalidate="true"
      :validated="validated"
      :disabled="isLoading || !inputState"
    >
      <div class="mb-3">
        <BFormFloatingLabel label="Enter code" label-for="otpInput" class="my-2">
          <BInput
            size="lg"
            v-model.trim="otpInput"
            id="otp"
            type="text"
            placeholder=""
            label="otpInput"
            maxlength="25"
            aria-autocomplete="none"
            autofocus
            autocomplete="off"
            lazy
            required
            :state="inputState"
          >
          </BInput>
          <BFormInvalidFeedback>Code should be 6 digits </BFormInvalidFeedback>
        </BFormFloatingLabel>

        <!-- <FormKit
